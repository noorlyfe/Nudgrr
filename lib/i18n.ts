const STORAGE_KEY = "@nudgrr/locale_override";

export async function getSavedLocale(): Promise<SupportedLocale | null> {
  const raw = await AsyncStorage.getItem(STORAGE_KEY);
  if (raw && raw in SUPPORTED_LOCALES) {
    return raw as SupportedLocale;
  }
  if (raw === "ar") {
    await AsyncStorage.removeItem(STORAGE_KEY);
  }
  return null;
}

export async function saveLocale(locale: SupportedLocale): Promise<void> {
  await AsyncStorage.setItem(STORAGE_KEY, locale);
}

export async function clearLocaleOverride(): Promise<void> {
  await AsyncStorage.removeItem(STORAGE_KEY);
}

export function getDeviceLocale(): SupportedLocale {
  const locale =
    Platform.OS === "ios"
      ? NativeModules.SettingsManager?.settings?.AppleLocale ||
        NativeModules.SettingsManager?.settings?.AppleLanguages?.[0] ||
        "en"
      : NativeModules.I18nManager?.localeIdentifier || "en";

  if (locale.startsWith("zh-Hant") || locale.startsWith("zh-TW") || locale.startsWith("zh-HK")) {
    return "zh-TW";
  }
  if (locale.startsWith("zh")) {
    return "zh";
  }
  const lang = locale.split(/[-_]/)[0] ?? "en";
  if (lang in SUPPORTED_LOCALES) {
    return lang as SupportedLocale;
  }
  return "en";
}

