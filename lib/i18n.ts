export function formatNumber(num: number, _locale: SupportedLocale): string {
  return num.toString();
}

export function formatDateMedium(iso: string, locale: SupportedLocale): string {
  try {
    return new Intl.DateTimeFormat(LOCALE_BCP47[locale] ?? "en-US", { dateStyle: "medium" }).format(
      new Date(iso)
    );
  } catch {
    return "";
  }
}

export function t(key: string, locale: SupportedLocale, params?: Record<string, string | number>): string {
  const dict = translations[locale] ?? translations.en;
  let str = dict[key] ?? translations.en[key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
}
