export function fillNudgeTemplate(template: string, amountFormatted: string): string {
  return template.replace(/\{amount\}/g, amountFormatted);
}

/** Full text for sharing (optional watermark for free tier). */
export function buildFullNudgeMessage(body: string, includeWatermark: boolean): string {
  return includeWatermark ? `${body}${NUDGE_MESSAGE_WATERMARK}` : body;
}
